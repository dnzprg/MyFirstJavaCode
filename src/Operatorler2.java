public class Operatorler2 {
    public static void main(String[] args) {
        int a = 30;
        int b = 40;

        // karsılastırma operatorleri
        // == , != , > , < , >= , <=

        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);

    }
}
