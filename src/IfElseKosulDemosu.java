public class IfElseKosulDemosu {
    public static void main(String[] args) {
        int saat = 1200;

        if (saat < 1200) {
            System.out.println("Saat öğleden önce");

        } else if (saat == 1200) {
            System.out.println("Saat öğle 1200");

        } else {
            System.out.println("Saat öğleden sonra");
        }
    }
}

